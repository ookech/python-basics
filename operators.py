#Arithmetic operators
x = 20
y = 6
print(x + y)
print(x % y) #modulus --divides and returns remainder as an output
print(x / y)
print(x - y)
print(x * y)

#comparison operators ---compares values
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #equal to
print(x != y) #not equal to